            <h1>What is a Cookie?</h1>
            <p>A cookie is a (sometimes) small piece of text consisting of different characters that is backed by a file
                somewhere on your computer that is accessible to your browser.</p>
            <p>
                The purpose of having cookies is that different websites sometimes want to track your browsing
                experience on their page; either to offer you a more customized experience, or to track you for other
                purposes. The cookie stores a (sometimes) encoded or encrypted string of characters that the website
                knows how to interpret as a fact about you; i.e. when was your last login? what time did you arrive on
                the page? do you have a free or a paid account? etc.
            </p>
        </section>

        <section>
            <h1>What types of cookies are there?</h1>
            <p>There are session cookies that are transient, and persistent cookies that are stored indefinitely.</p>
            <p>
                A session cookie is stored by your browser for the length of time that your web session is active. This
                could be either a predetermined length of time or until you logout and close your browser. These are
                commonly used to allow users to navigate between pages without resubmitting information like logging in
                again, or losing all the items in your cart.
            </p>
            <p>
                A persistent cookie lasts until it is deleted. These cookies are frequently used to retain user
                preferences for sites that are customizable. They also can be used to identify individual users within
                the web page. Some marketers place third party cookies in browsers for later identification
                purposes.
            </p>
        </section>

